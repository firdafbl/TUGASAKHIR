def balok(x, y, z):
    luas = 2 * (x * y) + (x * z) + (y * z)
    return luas

def kubus(s):
    luas = 6 * (s**3)
    return luas

def limas_segiempat(a, i):
    luas = a + i
    return luas

def bola(j):
    import math
    luas = 4 * math.pi * (j**2)
