def balok_volume(x,y,z):
    volume = (x * y * z)
    return volume

def kubus_volume(s):
    volume = (s * s * s)
    return volume

def limas_segiempat_volume(a, i):
    volume = (1/3) * a * i
    return volume

def bola_volume(j):
    import math
    volume = (4/3) * math.pi * (j**3)
    return volume